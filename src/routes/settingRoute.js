import express from "express";
import upload from "../middleware/upload.js";
import { addSettingController, deleteSettingController, fetchSettingController, updateSettingController } from "../controller/settingController.js";
import { verifyToken } from "../middleware/verifyToken.js";
const router=express.Router();
router.post("/addSetting",upload.single('photo'),verifyToken,addSettingController);
router.post("/updateSetting",upload.single('photo'),verifyToken,updateSettingController);
router.delete("/deleteSetting",upload.none(),verifyToken,deleteSettingController);
router.post("/fetchSetting",upload.none(),verifyToken,fetchSettingController);
export default router;