import express from "express";
import upload from "../middleware/upload.js";
import { changePassController, deleteUserController, fetchProfileController, forgotPassController, getAllUserController, loginController, registerController,updateProfileController } from "../controller/authController.js";
import { verifyToken } from "../middleware/verifyToken.js";
const router=express.Router();
router.post("/login",upload.none(),loginController);
router.post("/register",upload.none(),registerController);
router.post("/getProfile",upload.none(),verifyToken,fetchProfileController);
router.delete("/deleteUserById",upload.none(),verifyToken,deleteUserController);
router.post("/updateProfile",upload.single('photo'),verifyToken,updateProfileController);
router.post("/forgotPass",upload.none(),verifyToken,forgotPassController);
router.post("/getAllUser",upload.none(),verifyToken,getAllUserController);
router.post("/changePass",upload.none(),verifyToken,changePassController);
export default router;
